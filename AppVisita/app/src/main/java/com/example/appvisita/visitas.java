package com.example.appvisita;

public class visitas {


    private int id;
    private  int numero;

    private String observacao;
   private int id_estabelecimento ;



    private String data;
    private String irregularidade;
    private String recusa_receber;

    private int id_tipo_auto;
    private int id_fiscal;
    private String equipe;





    public visitas() {

    }


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getObservacao() {
        return observacao;
    }

    public void setObservacao(String observacao) {
        this.observacao = observacao;
    }

    public int getId_estabelecimento() {
        return id_estabelecimento;
    }

    public void setId_estabelecimento(int id_estabelecimento) {
        this.id_estabelecimento = id_estabelecimento;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getIrregularidade() {
        return irregularidade;
    }

    public void setIrregularidade(String irregularidade) {
        this.irregularidade = irregularidade;
    }

    public String getRecusa_receber() {
        return recusa_receber;
    }

    public void setRecusa_receber(String recusa_receber) {
        this.recusa_receber = recusa_receber;
    }

    public int getId_tipo_auto() {
        return id_tipo_auto;
    }

    public void setId_tipo_auto(int id_tipo_auto) {
        this.id_tipo_auto = id_tipo_auto;
    }

    public int getId_fiscal() {
        return id_fiscal;
    }

    public void setId_fiscal(int id_fiscal) {
        this.id_fiscal = id_fiscal;
    }

    public String getEquipe() {
        return equipe;
    }

    public void setEquipe(String equipe) {
        this.equipe = equipe;
    }
}





